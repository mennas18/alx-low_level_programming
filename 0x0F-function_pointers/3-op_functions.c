/**
 * opp_add - adds two int
 *
 * @a: the first int
 * @b: the second int
 *
 * Retuern: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * opp_sub - adds two int
 *
 * @a: the first int
 * @b: the second int
 *
 * Retuern: the difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * opp_mul - multibate two int
 *
 * @a: the first int
 * @b: the second int
 *
 * Retuern: the multibate
*/

int op_mul(int a, int b)
{
	return ( a * b);
}

/**
 * opp_div - adds two int
 *
 * @a: the first int
 * @b: the second int
 *
 * Retuern: the quotient
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * opp_mod - adds two int
 *
 * @a: the first int
 * @b: the second int
 *
 * Retuern: the reminder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
